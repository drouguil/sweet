	<p><b>Qu'est-ce que le XML ?</b></p>
	
	<p>Le XML est un langage informatique de balises (comme le HTML qui est le langage de mise en page des pages web), il permet de déclarer la couleur de certains éléments en jeu (comme le css mais il concenre d'autres éléments).</p>
	
	<p>Contrairement au CSS ici les couleurs sont déclarées en hexadécimal, ne vous inquiétez pas c'est beaucoup plus simple que cela en à l'air, vous pouvez trouver un generateur de couleur HTML (même lien que sur la page du CSS) <a href="http://html-color-codes.info/" target="_blank">ici</a>.</p>
	
	<p>Pour transformer un code couleur HTML en format pour le XML il suffit de changer le # en 0x, par exemple un #A35050 en 0xA35050 neanmoins il y a une option supplémentaire : l'opacité de la couleur, pour rajouter le pourcentage d'opacité vous devez rajouter la valeur d'opacité entre le 0x et le code couleur par exemple 0x60A35050.</p>
	
	<p>Pour aider dans le calcul du pourcentage d'opacité en hexacimal je vais vous donner le calcul à faire et un exemple (le x est la valeur d'opacité entre 0 et 100) : <b>Valeur_Entière(x*2.55/16) suivi de x*2.55%16</b></p>
	
	<p><b>ATTENTION (10 = A, 11 = B, 12 = C, 13 = D, 14 = E, 15 = F)</b></p>
	
	<p>Prenons 75% d'opacité on a donc : Valeur_Entière(75*2.55/16) suivi de 75*2.55%16 cela donne 0xBF suivi evidemment de la couleur que l'on souhaite.</p>
	
	<p>Chaque entrée (ligne commençant par &lt;entry&gt; et finissant par &lt;/entry&gt;) contient une clef (key) qui décrit ce qu'est sensé colorier l'entrée de la couleur écrite.
	Malheureusement il se peut que se soit assez obscur, il y a donc un tableau qui recense tous les éléments coloriées par les entrées (attention le tableau est en cours de remplissage, il risque d'être incomplet un petit moment)</p>
	
	<p>Sur ce, j'espère bien vous avoir expliqué le fonctionnement du XML en ayant rien oublié et bonne chance :)</p>
