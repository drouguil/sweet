	<p><b>Qu'est-ce que le JSON ?</b></p>
	
	<p>Le JSON est un format de données qui permet une représentation d'informations structurée, ici les fichiers JSON permettent de définir les propriétés de certains composants auxquels on intègre des textures.</p>
	
	<p>Il y a 4 fichiers JSON, qui sont à la racine du dossier : themeData.json, themeData-block.json, themeData-button.json, themeData-window.json</p>
	
	<p>Editez un des 4 fichiers pour observer le format des fichiers JSON, le principe du JSON est de construire une arborescence avec des objets, chaque objet commence par { et finit par } (vous pouvez donc remarquer que chaque fichier représente un seul objet), chaque objet contient soit un membre soit une liste de membres.<br>
	Un membre est du format "nom" : "valeur", une valeur peut être : un objet, un tableau, un litéral (chaîne, nombre, true, false, null).</p>
	
	<p>Prenons en exemple le fichier themeData.json, le premier membre est popup_border_frame qui dipose d'une liste de deux membres qui représentent les propriétés de ce membre, la valeur de ces deux membres représentent les valeurs des propriétés.</p>
	<p>De façon générale les propriétés sont :
	<ul>
		<li><b>uri</b> : chemin vers la texture</li>
		<li><b>scale9Grid</b> : un rectangle qui indique comment la texture doit être redimensionner (chercher scale 9 grid sur google). Exemple : scale9Grid:{x:[marge gauche], y:[marge haut], width:[taille - marge gauche - marge droite], height:[hauteur - marge haut - marge bas]}</li>
		<li><b>colorTransform</b> : Applique une transformation de couleur (voir Cliquez ici pour le détail des propriétés utilisables)</li>
		<li><b>align</b> : Alignement de la texture (TOPLEFT, TOP, TOPRIGHT, LEFT, CENTER, RIGHT, BOTTOMLEFT, BOTTOM, BOTTOMRIGHT)</li>
		<li><b>alpha</b> : Valeur de 0 à 1 indiquant la transparence (0 pour invisible, 1 pour sans transparence)</li>
		<li><b>bgColor</b> : Valeur de type 0xRRGGBB ou 0xAARRGGBB permettant de définir la couleur de fond du composant (voir le texte explicatif XML pour mieux comprendre le code couleur)</li>
		<li><b>bgAlpha</b> : Valeur de 0 à 1 indiquant la transparence du fond du composant (0 pour invisible, 1 pour sans transparence)</li>
	</ul>
	</p>
	
