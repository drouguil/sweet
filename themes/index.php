	<p><b>Qu'est ce que des textures ?</b></p>
	
	<p>En terme général des textures sont des éléments graphiques qui, regroupées, répresentent un objet plus important.<br>
	Ici les textures représentent les parties des interfaces en jeu. (Par exemple les boutons, les images de fond des interfaces etc...)<br>
	Les textures se trouvent dans les dossiers bitmap, common, illus et texture.</p>
	
	<p>Pour travailler sur les textures vous avez deux choix :
	<ul>
		<li>Télécharger un logiciel de traitement d'image (je vous conseille GIMP que vous pouvez télécharger via ce <a href="https://www.gimp.org/" target="_blank">lien</a>)</li>
		<li>Travailler sur un outil de traitement d'image en ligne (sur dofus tools il y a un très bon outil qui a été développé aucun téléchargement n'est nécessaire : <a href="http://www.dofus.tools/?page=themeMaker" target="_blank">lien ici</a>)</li>
	</ul>
	Pour actualiser les textures en jeu il faut fermer l'interface en cours de modification et la relancer (attention quelques fois cela ne marche pas et il faut fermer et relancer le launcher)<br>
	Pour savoir quelle texture modifie quel(s) élément(s) en jeu vous pouvez vous référencer au tableau ci-dessous (attention le tableau est en cours de remplissage, il risque d'être incomplet un bon moment vu le nombre de textures)<br>
	Sur ce, j'espère bien vous avoir expliqué le fonctionnement des textures en ayant rien oublié et bonne chance :)</p>
	
