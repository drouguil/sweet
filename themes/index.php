		<p>Vous pensez que la création de thèmes n'est réservée qu'aux connaisseurs en développement informatique et bien détrompez-vous !<br>
		
		Je vais vous apprendre en quelques explications tous les savoirs requis pour effectuer une telle tâche et enfin créer ce thème qui vous fait tant rêver ainsi que vous donner quelques conseils.<br>

		Mais avant toute chose il y a une règle essentielle en informatique : Ne jamais baisser les bras !</p>
		
		<p>Si vous vous lancez dans la création d'un thème mais que vous avez l'impression de pédaler dans la semoule n'hésitez pas à demander de l'aide sur le <a href="http://forum.dofus.com/fr/1578-themes-interfaces/2178871-themes-interface-vos-creations" target="_blank">Forum</a> ! Les autres joueurs vous vous aider il n'y a pas de honte à ne pas savoir faire quelque chose.</p>

		<p>Je vous garantie qu'il n'y a de meilleure récompense que la satisfaction personnelle que l'on obtient lorsqu'on arrive au bout d'un projet, aussi petit soit-il, dont on a mis toute notre motivation.<br>
		
		Maintenant que vous m'avez l'air motivés (même pas un petit peu ? ^^) passons aux instructions à suivre pour commencer un thème.</p>
		
		<p>Dans un premier temps il va falloir télécharger le client bêta si vous ne l'avez pas fait (<a href="http://forum.dofus.com/fr/1557-discussions-generales/1906976-telechargement-client-beta" target="_blank">lien ici</a>)</p>
		
		<p>Une fois ceci fait vous allez devoir passer en mode développement, le mode développement va vous permettre de débloquer de nouvelles fonctionnalités qui vous aideront lors du développement du thème. (Pour toute la suite du tuto une vidéo expliquant les tâches que j'explique à été faite par un joueur : <a href="https://www.youtube.com/watch?v=KD2q7o5ErDY" target="_blank">ici</a>)</p>
		
		<p>Pour se faire il faut :</p>
		
		<ul>
			<li>Aller dans l'explorateur de fichiers (c'est juste l'endroit où on se balade dans les dossiers de l'ordinateur)</li>
			<li>Tapez %appdata% (pour Windows) dans le chemin de navigation, vous allez vous retrouver sur C:/Users/nom_utilisateur/AppData/Roaming</li>
			<li>Revenez d'un cran en arrière et sélectionnez Local/Ankama/Dofus Beta/app (Vous êtes donc normalement sur C:/Users/nom_utilisateur/AppData/Local/Ankama/Dofus Beta/app)</li>
			<li>Si le fichier config-custom existe éditez-le sinon créez le</li>
			<li>Si le fichier est vide ou que vous venez de le créer ajoutez dedans : <b>&lt;langfile&gt; &lt;entry key="dev.mode"&gt;true&lt;/entry&gt; &lt;entry key="dev.auto.display.controler"&gt;true&lt;/entry&gt; &lt;/langfile&gt;</b> sinon rajoutez : <b>&lt;entry key="dev.mode"&gt;true&lt;/entry&gt; &lt;entry key="dev.auto.display.controler"&gt;true&lt;/entry&gt;</b></li>
			<li>Voilà le mode développement est activé, restez dans le dossier app on va en avoir besoin pour la suite</li>
		</ul>
		Maintenant voyons comment créer un thème, à partir du dossier app allez dans /content/themes ici seront stockées les thèmes personnalisés.
		
		<p>Pour créer son propre thème il faut :</p>
		
		<ul>
			<li>Faîtes une copie du dossier darkStone que vous appellerez par le nom de votre thème</li>
			<li>Entrez dans le dossier que vous venez de nommer</li>
			<li>Supprimez le fichier signature.xmls</li>
			<li>Renommer le fichier darkStone.dt par nom_de_votre_theme.dt (évidemment il faut remplacer nom_de_votre thème par le nom que vous avez mis au dossier ^^)</li>
			<li>Editez le fichier .dt (on va voir tout de suite ce qu'il faut modifier/ajouter)</li>
		</ul>
		
		<p>Dans le fichier .dt il y a plusieurs attributs mais il va falloir en rajouter d'autres, il vous faut donc :</p>
		
		<ul>
			<li><b>name</b> : Le nom de votre thème (identique au nom de votre dossier)</li>
			<li><b>description</b> : Texte de description de votre thème</li>
			<li><b>previewUri</b> : Nom de l'image qui apparaitera en tant qu'image vitrine de votre thème (elle doit se situer dans le dossier bitmap)</li>
			<li><b>author</b> : Votre pseudo ou prenom-nom enfin ce que vous voulez qui vous identifie</li>
			<li><b>version</b> : Version du thème (pour commencer il faut marquer 1.0.0, vous changerez lorsque vous effectuerez des modifications)</li>
			<li><b>dofusVersion</b> : Version du jeu compatible avec votre thème (la dernière version avec laquelle vous avez testé votre thème)</li>
			<li><b>creationDate</b> : date de création sous le format JJ/MM/AAAA</li>
			<li><b>modificationDate</b> : date de modification sous le format JJ/MM/AAAA</li>
		</ul>
		
		<p>Supprimez la ligne skinType.</p>
		
		<p>Vous pouvez maintenant commencer le développement de votre thème en suivant les autres onglets bonne chance :)</p>
	
