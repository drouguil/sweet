		<p><b>Qu'est-ce que le CSS ?</b></p>
		
		<p>Le CSS est un langage informatique qui décrit la présentation des documents, grâce à ce langage vous allez pouvoir gérer l'affichage d'éléments en jeu (attention pas tous, principalement les textes).</p>
		
		<p>Dans le dossier de votre thème vous disposez d'un dossier s'appelant css, c'est ici que vous allez retrouver l'ensemble des fichiers dans ce langage.<br>
		Maintenant que vous voyez à quoi vont servir ces fichiers nous allons voir leurs structure.
		Editez pour commencer le fichier chat.css avec un éditeur de texte (je vous conseille fortement d'utiliser notepad++ ou sublime texte si ce n'est pas fait car ils vont vous aider pour les syntaxes du langage. <a href="https://notepad-plus-plus.org/download/v7.2.html" target="_blank">Téléchargement Notepad++</a> - <a href="https://www.sublimetext.com" target="_blank">Téléchargement Sublime Texte</a>)</p>
		
		<p>Une fois ce fichier ouvert vous pouvez observer la structure du fichier qui sera similaire sur tous les fichiers css, la structure du fichier est donc p{ ... } p0_p{ ... } etc...<br>
		Nous appeleront tous les textes avant les accolades des classes (ne vous préoccupez pas si vous rencontrez des classes avec un # ou un point avant le nom de les classes laissez les comme ça), dans le fichier chat.css il y a donc 17 classes, chaque classe contient des informations relatives à cette classe.<br>
		Par exemple allez à la dernière classe du fichier (donc la classe bubble), cette classe va servir à gérer l'affichage du texte dans les bulles de dialogue.</p>
		
		<p>Voyons un peu les propriétés que donnent la classe au texte :
		<ul>
			<li><b>font-family: Tahoma;</b> -> Ceci précise que le texte est affiché dans la police de caractères Tahoma. Il y a 8 polices disponibles : BebasNeue, Eras, Gadugi, GoodyBookletter, Roboto, RobotoCondensed, Tahoma et Verdana.</li>
			<li><b>font-size: 18px;</b> -> Cela définit la taille du texte (attention en fonction la police la taille du texte peut beaucoup varier pour une même valeur), ici la valeur est en pixel (px) il y a d'autres unités mais je vous conseille de garder celle-là.</li>
			<li><b>color: #312D24;</b> -> Celui-ci détermine la couleur du texte via son code couleur, vous pouvez également déterminer le code couleur en nuances de rouge, vert, bleu. Par exemple ici ça donnerait <b>color: rgb(49, 45, 36)</b>, allez sur <a href="http://html-color-codes.info/" target="_blank"  >ce site</a> pour obtenir les couleurs que vous voulez.</li>
			<li><b>padding-left: -15;</b> -> Celui-là va effectuer un décalage du texte sur la gauche, je ne vous conseille pas de toucher aux décalages des textes mais si vous voulez le faire c'est très simple il suffit de mettre <b>padding-direction : valeur</b>, si vous rencontrez des problèmes d'élements qui ne suivent pas le décalage du texte remplacez padding par margin</li>
			<li><b>text-align: left;</b> -> Enfin on précise l'alignement du texte sur la gauche, si vous voulez mettre sur la droite c'est <b>right</b> et pour le centre <b>center</b>.</li>
		</ul>
		Il existe bien evidemment plein d'autres propriétés que l'on peut donner aux éléments, comme par exemple <b>font-weight: bold</b> pour mettre en gras ou <b>font-style: italic</b> pour mettre en italique.<br>
		Je vous en donne un petit dernier pour la route <b>text-decoration: underline</b> pour souligner un texte.<br>
		Une fois que vous avez modifié un fichier pour voir les modifications en jeu il faut soit taper la commande /clearcsscache et réouvrir l'interface modifiée ou relancer le launcher (ce qui implique de se reconnecter)</p>
		<p>Les textes entre /* et */ sont des commentaires c'est à dire qu'ils ne sont pas interprétés, ceux sont uniquement du texte afin d'aider à la relecture des fichiers (pour s'y retourver plus facilement), n'hésitez-pas à en mettre pas mal.<br>
		Si vous voulez l'ensemble des autres propriétés allez sur une documentation complète comme <a href="http://www.w3schools.com/cssref/" target="_blank">ici</a> (attention tout est en anglais)<br>
		Grâce au tableau si dessous vous allez pouvoir chercher les correspondances entre les classes de chaque fichier et leur élément en jeu (attention le tableau est en cours de remplissage, il risque d'être incomplet un bon moment vu le nombre de classes)</p>
		
		<p><b>ATTENTION</b> de bien mettre un ; à la fin de chaque instruction sinon cela ne fonctionnera pas !<br>
		Sur ce, j'espère bien vous avoir expliqué le fonctionnement du CSS en ayant rien oublié et bonne chance :)</p>
